卡标识（CID）寄存器的宽度为128位宽。它包含在卡识别阶段使用的卡识别信息。每张读/写（RW）卡应具有唯一的标识号。CID寄存器的结构在以下段落中定义：



SD256[103:64]位(5344323536h)*/






